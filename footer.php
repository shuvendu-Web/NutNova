<script>
    document.addEventListener("DOMContentLoaded", () => {
        const accountStatus = document.getElementById("account-status");
        const user = localStorage.getItem("username");

        // ✅ If logged in: Show username + hover logout menu
        if (user) {
            accountStatus.innerHTML = `
      <div class="account-dropdown">
        <a href="#" class="account-name"><i class="zmdi zmdi-account"></i><span>&nbsp; ${user}</span></a>
        <ul class="account-menu">
          <li><a href="#" onclick="logoutUser()">Sign Out</a></li>
        </ul>
      </div>
    `;
        } else {
            // ❌ Not logged in: clicking redirects to login.php
            accountStatus.innerHTML = `
      <div class="account-dropdown">
        <a href="login.php" class="account-name"><i class="zmdi zmdi-account"></i><span>&nbsp; Log in</span></a>
      </div>
    `;
        }

        // 🔐 Login form handling
        const loginForm = document.getElementById("login-form");
        if (loginForm) {
            loginForm.addEventListener("submit", function(e) {
                e.preventDefault();
                const username = document.getElementById("name").value.trim();
                const password = document.getElementById("password").value.trim();

                if (!username || !password) {
                    alert("Both fields are required.");
                    return;
                }

                localStorage.setItem("username", username);
                alert(`Welcome, ${username}!`);
                window.location.href = "index.php"; // go home after login
            });
        }
    });

    function logoutUser() {
        localStorage.removeItem("username");
        window.location.reload();
    }
</script>